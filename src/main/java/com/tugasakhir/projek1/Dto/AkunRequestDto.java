package com.tugasakhir.projek1.Dto;

public class AkunRequestDto {
	private String nama_akun;
	private Integer nominal;
	private String ket_akun;
	private Integer month;
	private Integer year;
	public String getNama_akun() {
		return nama_akun;
	}
	public void setNama_akun(String nama_akun) {
		this.nama_akun = nama_akun;
	}
	public Integer getNominal() {
		return nominal;
	}
	public void setNominal(Integer nominal) {
		this.nominal = nominal;
	}
	public String getKet_akun() {
		return ket_akun;
	}
	public void setKet_akun(String ket_akun) {
		this.ket_akun = ket_akun;
	}
	public Integer getMonth() {
		return month;
	}
	public void setMonth(Integer month) {
		this.month = month;
	}
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	
	

}
